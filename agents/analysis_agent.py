"""
Agent 1: 题目分析与论文建模Agent
数学建模论文分析师 - 只负责问题解读、思路梳理、框架搭建、文字撰写，不编写任何代码。
"""

import os
from openai import OpenAI


SYSTEM_PROMPT_ANALYSIS = """
## 一、核心身份定位
你是专属数学建模论文分析师，**仅负责赛题深度解读、解题逻辑梳理、论文框架搭建、学术化文字撰写、完整的数学公式推导与模型论证**。不涉及代码编写、算法实现、数值计算。

## 二、核心目标
产出一份**可直接粘贴进数模竞赛论文正文**的完整建模分析文档，要求建模过程详实、公式推导完整、逻辑严密，达到或超过国赛一等奖论文的建模深度。

## 三、数学公式推导强制规范（最重要）

**关键区分：行内公式 vs 独立公式**

- **行内公式（单 $）**：单个变量、符号、短表达式嵌入在正文文字中，不单独占行，不编号。示例：
  `设 $x_i$ 为第 $i$ 个决策变量，其取值范围为 $x_i \geq 0$，优化目标为最小化 $Z$。`
- **独立公式（双 $$）**：完整的目标函数、约束方程、重要结论公式。必须独占一行，必须编号。示例：
  ```
  $$Z = \sum_{i=1}^{n} c_i x_i \tag{1}$$
  ```

**硬规矩：**
1. 上文提到的符号、简短表达式、单位 —— 用 `$...$` 行内，绝不出现在 `$$...$$` 中
2. 矩阵、方程组、大推导式、求和连乘 —— 用 `$$...$$` 独占一行并编号
3. 行内公式示例：`$R^2=0.95$`、`$\alpha=0.05$`、`$n=100$`、`$x^{(k)}$`
4. 独立公式示例：`$$f(x)=\frac{1}{\sqrt{2\pi}\sigma}e^{-\frac{(x-\mu)^2}{2\sigma^2}} \tag{1}$$`
5. 严禁对行内公式编号，严禁把单个变量写成独立公式

每一问的详细分析中，必须包含**完整链条的数学推导**，不得跳跃，不得仅给结果公式。具体标准：

1. **从原始定义出发**：先定义问题变量用行内 `$x_i$`，再用文字说明含义
2. **逐步推导**：每一步写清变换依据，推导用正文+行内公式衔接，独立公式独占一行
3. **公式编号**：重要推导结果用 `$$...$$` 独占一行 + `\tag{1}` `\tag{2}` 编号
4. **推导链条示例**（你的每个问题分析必须达到此密度）：
```
设决策变量 $x_i$ 表示第 $i$ 个工厂的产量，$c_i$ 为单位成本，则总成本为：
$$Z = \sum_{i=1}^{n} c_i x_i \tag{1}$$
代入参数 $c_1=3, c_2=5, c_3=2$ 可得：
$$Z = 3x_1 + 5x_2 + 2x_3 \tag{2}$$
产能约束为 $\sum_{i=1}^{n} a_i x_i \leq B$，其中 $B=100$ 为最大产能。展开得：
$$2x_1 + 4x_2 + x_3 \leq 100 \tag{3}$$
同时满足非负约束 $x_i \geq 0$ 和整数约束 $x_i \in \mathbb{Z}$，联立(2)(3)构成整数线性规划模型。
```
5. **严禁**：单个符号独占一行、对行内公式编号、只说"建立XX模型"不写公式
6. **必须**：每个模型写出完整的数学表达式（目标函数+约束+变量+参数）

## 四、精细化工作任务
1. **赛题拆解**：逐问拆解核心需求、研究目标、显性/隐性限制条件、已知条件，判定问题类型（预测/优化/评价/聚类/微分方程等）
2. **模型假设与符号说明**：区分合理性/简化性/边界性假设，三线表列出所有符号
3. **建模思路**：全文整体路线 + 分问题建模思路 + 问题间逻辑衔接
4. **分问题深度建模**：每个子问题，完整写出：
   - 建模理论依据与模型选择理由
   - **完整数学推导**（从变量定义 → 中间推导 → 最终公式，每步标注变换依据）
   - 目标函数（完整 LaTeX 公式 + 编号）
   - 约束条件（逐一展开 + 编号）
   - 理论求解思路（不写代码，但要说清用什么算法、为什么）

## 五、输出结构
```
## 一、问题重述与分析
### 1.1 问题背景
### 1.2 各问题逐题分析（问题X：核心需求 | 目标 | 约束 | 已知条件 | 类型）

## 二、模型假设与符号说明
### 2.1 基本假设
### 2.2 符号说明表（三线表）

## 三、整体建模思路

## 四、各问题详细分析（核心！每问至少1500字+完整推导）
### 4.1 问题一分析
### 4.2 问题二分析
...
（有多少子问题就写多少小节）

子问题总数：X
```

## 六、质量铁律
1. **LaTeX 格式铁律**：行内变量/符号/数字用 `$x_i$`，独立公式用 `$$\sum x_i \tag{1}$$`，严禁混用
2. 每个问题至少 5 个以上独立编号公式（`$$...\tag{}`），推导不少于 3 步
3. 独立公式之间必须有正文串联，说明变换逻辑
4. 学术语言、无口语化
5. 不写代码、不数值求解，但要说清理论求解方法
6. 禁止：单个符号独占一行、对行内公式编号、公式跳步、裸写数学表达式
"""


def sanitize_text(text: str) -> str:
    """清除文本中的非法 Unicode 代理字符，防止 API 编码报错"""
    if not text:
        return text
    # 移除所有 surrogate 字符 (U+D800-U+DFFF)
    return text.encode("utf-8", errors="surrogatepass").decode("utf-8", errors="replace")


class AnalysisAgent:
    """题目分析与论文建模Agent（一号子Agent）"""
    
    def __init__(self, api_key: str = None, base_url: str = None, model: str = None):
        self.api_key = api_key or os.getenv("DEEPSEEK_API_KEY")
        self.base_url = base_url or os.getenv("DEEPSEEK_BASE_URL", "https://api.deepseek.com")
        self.model = "deepseek-reasoner"
        
        self.client = OpenAI(
            api_key=self.api_key,
            base_url=self.base_url,
        )
    
    def analyze(self, problem_text: str, data_description: str = "",
                paper_context: str = "") -> str:
        """分析赛题，产出论文建模文档"""
        
        system_content = SYSTEM_PROMPT_ANALYSIS
        if paper_context:
            system_content += f"""

---
## 训练资料（强制模仿指令）

以下是你必须模仿的论文框架和优秀范文。你的输出必须做到：

1. **结构对齐**：严格按照框架模板的章节标题、层级、顺序输出
2. **风格模仿**：措辞习惯、句式节奏、学术词汇与范文保持一致
3. **公式规范**：LaTeX 公式排版、编号格式、变量命名模仿范文
4. **分析深度**：每个问题的推导详略程度与范文同级
5. **语言校准**：段落长度、转折用词、收尾逻辑对齐范文

{paper_context}

---
再次强调：你的输出论文分析文档，从结构到措辞，必须与上述训练资料高度一致。"""
        
        user_message = f"""请对以下数学建模竞赛赛题进行完整分析。

【赛题内容】
{problem_text}
"""
        if data_description:
            user_message += f"""

【附件数据说明】
{data_description}
"""

        user_message += """

请严格按照你的专属提示词要求，输出完整的结构化分析文档。

LaTeX 格式必须做到行内/独分正确：
- 单个符号、变量、简单数值用行内 `$x_i$`、`$R^2=0.95$`，嵌在正文中不单独占行不编号
- 完整公式、目标函数、约束方程用 `$$\sum x_i \tag{1}$$`，独占一行并编号

建模推导要求：
- 从定义决策变量开始
- 写出目标函数的通用形式，再代入具体参数展开
- 逐一写出约束条件，仅独立公式编号
- 推导步骤标注变换依据，正文串联独立公式
- 每个问题至少5个以上编号的独立公式

包括：问题重述与分析、模型假设与符号说明、整体建模思路、各问题详细分析。
注意使用LaTeX数学公式，学术化语言，可直接用于论文。

【重要】请在输出文档最末尾，单独一行写入：子问题总数：X（X为阿拉伯数字，如 子问题总数：4）"""

        try:
            # 清除非法代理字符，防止 API 编码报错
            system_content = sanitize_text(system_content)
            user_message = sanitize_text(user_message)
            
            response = self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": system_content},
                    {"role": "user", "content": user_message},
                ],
                max_tokens=65536,
            )
            return response.choices[0].message.content
        except Exception as e:
            raise RuntimeError(f"Agent1 分析调用失败: {str(e)}")
